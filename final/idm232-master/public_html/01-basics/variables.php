<?php
// Casing
$item = 'Item';
$Items = 'Items';

// CamelCase
$myVariable = 'some string';
$myFirstName = 'some string';

// Underscore
// $my-variable = 'some string';
$my_variable = 'some string';

// Numbers
// $3variable = 'some string';
$variable3 = 'some string';
$v232234234234 = 'some string';

// Best Practice
// Avoid:
// - hyphenated variable names (`$this-variable`)
// - multiple underscores (`$__bookPage`)
// - leading underscore (`$_book`)
